public class Cat {
    private String name;
    private int age;

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return this.age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void walk() {
        System.out.println("walking...");
    }

    public void sleep() {
        System.out.println("sleeping...");
    }


}
